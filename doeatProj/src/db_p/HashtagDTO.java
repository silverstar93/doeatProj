package db_p;

public class HashtagDTO {
	String memNo, hcon;
	int no,hashcode;
	
	public String getMemNo() {
		return memNo;
	}
	public void setMemNo(String memNo) {
		this.memNo = memNo;
	}
	public String getHcon() {
		return hcon;
	}
	public void setHcon(String hcon) {
		this.hcon = hcon;
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	public int getHashcode() {
		return hashcode;
	}
	public void setHashcode(int hashcod) {
		this.hashcode = hashcod;
	}
	
	
}
